<h3 id="memoire-saint-michel" class="red">聖ミカエルの記憶祝日</h3>

<p>第四に、この祝日は、聖<mark>ミカエル</mark>の記憶ともよばれる。</p>
<p>なぜなら、われわれは、この日すべての天使たちの記憶をおこない、天使たちすべてをともに崇敬するからである。</p>
<p>われわれが天使たちに称賛と栄誉をたてまつるのは、多くのことのためである。</p>
<ul>
  <li>天使は、われわれの守護者であり、</li>
  <li>奉仕者であり、</li>
  <li>兄弟であり、</li>
  <li>同胞である。</li>
  <li>天使たちはまた、われわれのたましいを天にはこんでいき、</li>
  <li>われわれの祈りを神のおんまえにとどけてくれる。</li>
  <li>天使たちは、至高の王のけだかい騎士であり、</li>
  <li>悲しめる者たちの慰め手である。</li>
</ul>

<h4 id="anges-gardiens" class="red">天使たちがわれわれの守護者だからである</h4>

<p>われわれが天使たちを崇敬しなくてはならないのは、まず第一に、彼らがわれわれの守護者だからである。
それぞれの人間には、ふたりの天使がついている。
われわれを誘惑する悪天使とわれわれを見張ってくれる善天使とである。
善天使は、誕生のときからずっとわれわれについていてくれる。</p>
<p>つまり、母の胎内にやどったときから、さらに母の胎内を出るときも、大人になってからも、ずっと人間についていてくれるのである。</p>
<p>人間は、この三つの時期にわたって天使に守ってもらわなくてはならない。
<ul>
  <li>
    <p>母の胎内にいるまだ小さいときでも、すでに死と地獄落ちの危険があるし、</p>
  </li>
  <li>
    <p>生まれてからもまだ幼いころは、洗礼を受けられなくなる危険がある。</p>
  </li>
  <li>
    <p>大人になってからは、さまざまな罪の<ruby>罠<rp>(</rp><rt>わな</rt><rp>)</rp></ruby>にかかる心配がある。</p>
  </li>
</ul>
<p>悪魔は、大人の人間を試みるのが好きである。
分別を<ruby>奸<rp>(</rp><rt>かん</rt><rp>)</rp>計<rp>(</rp><rt>けい</rt><rp>)</rp></ruby>でまどわせたり、意志を誘惑でそそのかしたり、長所を力ずくで抑えつけたりするのである。</p>
<p>こうした理由から、人間に守護の天使をつけてやる必要があるのである。</p>
<p>天使は、人間を育て、悪魔の奸計に負けないようにみちびき、悪魔の誘惑に抗して善行をうながし、悪魔の暴力と抑圧に屈しないように助けてくれるのである。</p>
<p>天使が守ってくれた結果が人間のなかにどのようにあらわれるかと言うと、四通りのあらわれかたがあると理解することができる。</p>
<p>第一のあらわれは、人間のたましいが<ruby>聖<rp>(</rp><rt>せい</rt><rp>)</rp>寵<rp>(</rp><rt>ちょう</rt><rp>)</rp></ruby>を受けて善にむかって成長していくことである。</p>
<p>天使は、これを三つのしかたでおこなう。</p>
<ul>
  <li>
    <p>まず、天使は、善をさまたげるすべてのものを滅ぼす。
    『出エジプト記』（１２・２８以下）で天使がエジプト人のすべての<ruby>初<rp>(</rp><rt>うい</rt><rp>)</rp>子<rp>(</rp><rt>ご</rt><rp>)</rp></ruby>を打つのが、それである。</p>
  </li>
  <li>
    <p>つぎに、天使は、われわれからすべての怠情をとりのぞく。
    『ザカリア書』に「主の<ruby>御<rp>(</rp><rt>み</rt><rp>)</rp></ruby><ruby>使<rp>(</rp><rt>つか</rt><rp>)</rp></ruby>いは、眠っている人を起こすように、わたしを呼びさました」（４・１）と書かれているのが、それである。</p>
  </li>
  <li>
    <p>天使は、さらに人間を<ruby>悔<rp>(</rp><rt>かい</rt><rp>)</rp>悛<rp>(</rp><rt>しゅん</rt><rp>)</rp></ruby>の道にみちびき、またつれて帰る。
  『トビト書』（５章）で天使がトビアスの旅の道案内をつとめるのが、それである。</p>
  </li>
</ul>
<p>第二のあらわれは、人間が罪に落ちなくなることである。
天使は、人間が罪に落ちるのを三つのしかたでさまたげる。</p>
<ul>
  <li>
    <p>まず、未来の災いをまえもって<ruby>阻<rp>(</rp><rt>そ</rt><rp>)</rp>止<rp>(</rp><rt>し</rt><rp>)</rp></ruby>する。
    『民数記』（２２・２４）でイスラェルを呪うために家を出た<a href="#note-balaam" title="バラーム" id="balaam">バラーム</a>を天使が途中でさまたげたと書かれているのが、それである。</p>
  </li>
  <li>
    <p>つぎに、過去の罪を罰し、人間をそれから解きはなつ。
    『<ruby>士<rp>(</rp><rt>し</rt><rp>)</rp>師<rp>(</rp><rt>し</rt><rp>)</rp>記<rp>(</rp><rt>き</rt><rp>)</rp></ruby>』（２・１～４）に、天使がイスラエルの子らを主の命令にしたがわなかったことでとがめると、人びとは声をあげて泣いたと書かれているのが、それである。</p>
  </li>
  <li>
    <p>さらに、現在の罪を断固として<ruby>阻<rp>(</rp><rt>はば</rt><rp>)</rp></ruby>む。
    これは、天使が<a href="#note-loth" title="ロト" id="loth">ロト</a>とその妻をソドム（すなわち罪の悪習）からむりやりにつれだしたことのなかにしめされている。</p>
  </li>
</ul>
<p>第三のあらわれは、人間が罪に落ちても再起することである。
天使は、これも三つのしかたでおこなう。</p>
<ul>
  <li>
    <p>まず、天使は、人間に<ruby>痛<rp>(</rp><rt>つう</rt><rp>)</rp>悔<rp>(</rp><rt>かい</rt><rp>)</rp></ruby>をさせる。
    『トビト書』（１１・１１）に、トビアスが天使に教えられて父親の眼（すなわち、霊の眼）に<ruby>胆<rp>(</rp><rt>たん</rt><rp>)</rp>汁<rp>(</rp><rt>じゅう</rt><rp>)</rp></ruby>を塗る話が出てくるが、これが痛悔なのである。</p>
  </li>
  <li>
    <p>つぎに、天使は、告解をさせるために人間のロを清める。『イザヤ書』（６・６～７）に、天使が<a href="#note-isaie" title="イザヤ" id="isaie">イザヤ</a>のロを清める話出ている。</p>
  </li>
  <li>
    <p>さらに、天使は、人間を悔俊させる。
    これについては、『ルカによる福音書』（１５・７）に、ひとりの罪びとが悔いあらためたならば、天は九十九人の正しい人にたいするよりもよろこばれるであろう、と書かれている。</p>
  </li>
</ul>
<p>四つ目のあらわれは、悪魔がしかけるような大きな、また多くの罪に人間がもはや落ちなくなることである。
天使は、これも三つのしかたでおこなう。</p>
<ul>
  <li>
    <p>まず悪魔の力を弱め、</p>
  </li>
  <li>
    <p>つぎに悪の欲望を抑え、</p>
  </li>
  <li>
    <p>最後に、すでに述べたように、主のご受難の記憶をわたしたちの意識にしっかりおぼえこませる。</p>
  </li>
</ul>

<h4 id="anges-servants" class="red">天使たちがわれわれの奉仕者だからである</h4>

<p>われわれが天使たちを崇敬しなくてはならない第二の理由は、彼らがわれわれの奉仕者だからである。</p>
<p>というのは、『ヘブル人への手紙』にもあるように、</p>
<p class="conversation">天使たちはみな、「仕える霊」（１・１４）だからである。</p>
<p class="conversation">彼らはみな、われわれに奉仕するために遣わされる。</p>
<ol>
  <li>
    <p>上級の天使たちは、中級の天使たちのもとに、</p>
  </li>
  <li>
    <p>中級の天使たちは、下級の天使たちのもとに、</p>
  </li>
  <li>
    <p>そして、下級の天使たちは、われわれ人間のもとに遣わされる。</p>
  </li>
</ol>
<p>しかし、この派遣は、</p>
<ol>
  <li>
    <p>神の慈悲である。
    神は、われわれの救霊のためにいと深い愛によって神ご自身とむすばれている最も高貴な霊たちをわれわれのもとに遣わされるのであるから、これは、とりもなおさず神の慈悲というものである。</p>
  </li>
  <li>
    <p>この派遣はまた、天使たちの愛でもある。
    というのは、他人の浄福をもとめるのは、すなわち燃える愛というものではないであろうか。
    預言者イザヤも、天使たちとおなじ愛に燃えていたので、</p>
    <p class="conversation">「ここにわたしがおります。主よ、わたしをお遣わしください」（『イザヤ』六の八）と言うのである。</p>
    <p>天使たちも、われわれが悪魔と戦いながら彼らの助けを必要としているのを見ると、助けに行かずにはおれなくなる。つまり、天使たちの愛の律法は、われわれのもとに派遣されたときに<ruby>成<rp>(</rp><rt>じょう</rt><rp>)</rp>就<rp>(</rp><rt>じゅ</rt><rp>)</rp></ruby>するので。</p>
  </li>
  <li>
    <p>さらに、天使たちの派遣は、われわれの弱さのためでもある。</p>
    <ol>
      <li>
        <p>つまり、天使たちが遺わされるのは、まず第一に、われわれの愛の願いに火を点じるためである。
        よく天使たちが火の車に乗って遣わされてきたと書いているのは、そのことを比喩的に言ったのである。</p>
      </li>
      <li>
        <p>第二に、われわれの暗い精神に光をあてて認識を得させるためである。
        『黙示録』（１０・２）に語られている、開かれた本を手にした天使は、そのことをあらわしている。</p>
      </li>
      <li>
        <p>第三に、われわれのなかにあるすべての完全なものをいやがうえにも強め、鼓舞するためである。『列王紀上』（１９・５～８）に、天使がエリアに焼け石のうえで焼いたパンと<ruby>一<rp>(</rp><rt>ひと</rt><rp>)</rp>瓶<rp>(</rp><rt>びん</rt><rp>)</rp></ruby>の水をあたえ、これを食べて力づいたエリアがついに神の山<a href="#note-horeb" title="ホレブ" id="horeb">ホレブ</a>に着いたという話が出てくるが、これはそのことをあらわしている。</p>
      </li>
    </ol>
  </li>
</ol>

<h4 id="anges-freres" class="red">天使たちがわれわれの兄弟であり、同胞であるためである</h4>

<p>われわれが天使たちを尊崇しなければならない第三の理由は、彼らがわれわれの兄弟であり、同胞であるためである。</p>
<p>というのは、すべての選ばれた者は、天使たちの群れのなかに加えられるからである。
<ol>
  <li>
    <p>上級の隊に加えられる者もあれば、</p>
  </li>
  <li>
    <p>中級の、</p>
  </li>
  <li>
    <p>あるいは下級の隊に</p>
  </li>
</ol>
<p>入れられる者もある。これは、それぞれの<ruby>功<rp>(</rp><rt>く</rt><rp>)</rp>徳<rp>(</rp><rt>どく</rt><rp>)</rp></ruby>の違いによる。ただし、天使の全軍勢よりも上位に挙げられた聖母だけは、ベつである。</p>
<p>これについては、聖グレゴリウスも、ある説教のなかでつぎのように語っている。</p>
<ol>
  <li>
    <ol>
      <li>
        <p class="conversation">「もって生まれた<ruby>天<rp>(</rp><rt>てん</rt><rp>)</rp>賦<rp>(</rp><rt>ぷ</rt><rp>)</rp></ruby>のものはごくわずかでも、それをなおかつ隣人たちに分かちあたえる人たちがいる。
        こういう人たちは、いちばん下位の天使たちの仲間に加えられる。</p>
      </li>
      <li>
        <p class="conversation">天主にかかわる事柄の深い隠れた意味を理解し、それを世に教える人たちがいる。
        この人たちは、大天使のところまでのぼっていく。</p>
      </li>
      <li>
        <p class="conversation">しるしをおこない、大きな力をもって世に尽くす人たちがいる。
        この人たちは、<ruby>力天使<rp>(</rp><rt>ウイルトウテス</rt><rp>)</rp></ruby>の仲間になる。</p>
      </li>
    </ol>
  </li>
  <li>
    <ol>
      <li>
        <p class="conversation">祈りの力と自分にあたえられている権能で悪霊を追いはらう人たちがいる。
        彼らは、<ruby>能天使<rp>(</rp><rt>ポテスタテス</rt><rp>)</rp></ruby>の仲間になる。</p>
      </li>
      <li>
        <p class="conversation">自分にあたえられたもろもろの力で聖人たちの功績をもしのぎ、選ばれた兄弟たちに命令をくだす人たち。
        こういう人たちは、権大使の仲間になる。</p>
      </li>
      <li>
        <p class="conversation">みずからの内部のあらゆる悪徳を克服し、その清らかさのために世人から神とも仰がれている人たち。
        主がモーセにむかって、〈わたしは、あなたをファラオにたいして神のごときものとする〉（『出エ』７・１）と言われた言葉にふさわしいような人たち。
        こういう人たちは、<ruby>主天使<rp>(</rp><rt>ドミナテイオネス</rt><rp>)</rp></ruby>とともに住む。</p>
      </li>
      <li>
        <p class="conversation">主が<ruby>御<rp>(</rp><rt>み</rt><rp>)</rp>座<rp>(</rp><rt>ざ</rt><rp>)</rp></ruby>にすわるようにその内部にやどって、人びとの行為をお裁きになる人たち。
        聖会を治め、すべての信者たちの弱きわざを裁く人たち。
        この人たちは、<ruby>座天使<rp>(</rp><rt>トロネ</rt><rp>)</rp></ruby>のもとに行く。</p>
      </li>
    </ol>
  </li>
  <li>
    <ol>
      <li>
        <p class="conversation">ほかのすべての人びとにまさって神と隣人にたいする愛にみちみちている人たち。
        彼らは、<ruby>智天使<rp>(</rp><rt>ケルビム</rt><rp>)</rp></ruby>のなかに加えられる。
        というのは、<ruby>智天使<rp>(</rp><rt>ケルビム</rt><rp>)</rp></ruby>とは、知恵にみちていることであり、律法をみたし実行することは、パウロ（８５章）によれば、愛だからである。</p>
      </li>
      <li>
        <p class="conversation">さらに、永遠の観想にたいする愛に燃え、造物主を観想することのほかにはなにも求めない人たちがいる。
        彼らは、この世ではもはやなにも望まず、永遠への愛によってのみ生き、いっさいの地上のものを脱ぎすてる。
        彼らのこころは、現世のはるかかなたにむけられている。
        彼らは、愛し、燃えたち、みずからの愛の炎のなかにやすらっている。
        彼らの愛は、火であり、彼らの言葉は、ほむらである。
        そして、その言葉のとどくかぎり、<ruby>森<rp>(</rp><rt>しん</rt><rp>)</rp>羅<rp>(</rp><rt>ら</rt><rp>)</rp>万<rp>(</rp><rt>ばん</rt><rp>)</rp>象<rp>(</rp><rt>しょう</rt><rp>)</rp></ruby>を神への愛の<ruby>火<rp>(</rp><rt>か</rt><rp>)</rp>焰<rp>(</rp><rt>えん</rt><rp>)</rp></ruby>のなかに燃えあがらせる。
        この人たちの召されていくところは、<ruby>熾天使<rp>(</rp><rt>セラピム</rt><rp>)</rp></ruby>のもと以外のどこにあるだろうか」</p>
      </li>
    </ol>
  </li>
</ol>
<p>聖グレゴリウスは、このように述べている。</p>

<h4 class="red">天使たちがわれわれのたましいを天にみちびいてくれるためである</h4>

<p>われわれが天使たちを尊崇しなくてはならない第四の理由は、天使たちがわれわれのたましいを天にみちびいてくれるためである。
天使たちは、これを三つの手順をふんでおこなう。</p>
<ol>
  <li>
    <p>まず第一に、彼らは、道を準備する。
    これについては、『マラキ書』に、「見よ、わたしは、わが天使を遣わす。
    彼は、わたしのまえに道を備える」（３・１）とある。</p>
  </li>
  <li>
    <p>つぎに、天使たちは、この備えられた道を通ってわれわれのたましいを天にみちびいていく。
    『出エジプト記』に、</p>
    <p class="conversation">「見よ、わたしは、使いをあなたのまえに遣わし、あなたの道を守らせ、わたしが備えたところにみちびかせるであろう」（２３・２０）とあるのが、それである。</p>
  </li>
  <li>
    <p>さらに、天使たちは、たましいを天国に入れる。
    「ルカによる福音書」に、</p>
    <p class="conversation">「やがて、この貧しい人は死に、御使いたちによってアブラハムの<a href="#note-hutokoro" title="ふところ" id="hutokoro">ふところ</a>につれていかれた」（１６・２２）とあるのが、それである。</p>
  </li>
</ol>

<h4 id="anges-intercesseurs" class="red">天使たちがわれわれの祈りを神のまえにとどけてくれるためである</h4>

<p>われわれが天使たちを崇敬しなくてはならない第五の理由は、彼らがわれわれの祈りを神のまえにとどけてくれるためである。
れにも、三つの手順がある。</p>
<ol>
  <li>
    <p>まず、天使たちは、われわれの祈りを主のまえにもっていく。
    『トビト書』に、</p>
    <p class="conversation">「あなたが涙ながらに祈り、死者を葬ったとき、わたしは、あなたの祈りを神のまえにもっていった」（１２・１２）とあるのが、それである。</p>
  </li>
  <li>
    <p>つぎに、天使たちは、神のまえでわれわれのためにとりなしてくれる。
    『ヨブ記』に、</p>
    <p class="conversation">「もし千人の天使たちのうちのひとりがその人のためにとりなし、その人の義しさを告げてくれるならば、神は、その人をあわれまれる」（３３・２３）とある。</p>
    <p>また、「ザカリア書」には、</p>
    <p class="conversation">「すると、主の御使いは言った。
    〈万軍の主よ、あなたは、いつまでイスラエルと<a href="#note-juda" title="ユダ" id="juda">ユダ</a>の町をあわれんでくださらないのですか。
    あなたがお怒りになってから、もう７０年にもなります〉」（１・１２）とあるが、使は、こう言ってイスラエルをとりなしたのである。</p>
  </li>
  <li>
    <p>三番目に、天使たちは、神のご判断をわれわれに告げる。
    『ダニエル書』に、お告げの天使ガブリエルがダニエルのところに飛んできて、</p>
    <p class="conversation">「あなたが祈りはじめたとき、主のみ言葉が出た」（９・２３）と告げるところがあるが、<ruby>注解書<rp>(</rp><rt>グロツサ</rt><rp>)</rp></ruby>は、「これが神のご判断なである」と述べている。
    『ダニエル書』は、そのあとさらに「わたしは、それをあなたに告げるために来ました。
    あなたは、大いに愛せられている者です」と記している。</p>
  </li>
</ol>
<p>聖べルナルドゥスは、『<a href="#note-psaumes" title="聖歌" id="psaumes">雅歌</a>』について述べたくだりでこの3つの手順をこう言っている。</p>
<p class="conversation">「天使は、愛しあうふたりのあいだを駆けめぐり、花嫁の誓いを花婿に伝え、花婿の贈りものを花嫁にとどけ、花嫁に恋心を呼びさまし、花婿の気持を花嫁にやさしくさせる」。</p>

<h4 id="anges-chevaliers" class="red">天使たちが永遠の王のけだかい騎士であるからである</h4>

<p>われわれが天使を崇敬する六つ目の理由は、天使たちが永遠の王のけだかい騎士であるからである。
『ヨブ記』には、</p>
<p class="conversation">「その軍勢は、教えることができようか」（２５・３）とある。</p>
<p>ところで、だれでも知っているように、世俗の王の配下の騎士たちのなかには、</p>
<ol>
  <li>
    <p>いつも宮廷にいて、王の相手をつとめ、王をたたえ慰める歌をうたう者もあれば、</p>
  </li>
  <li>
    <p>町や城を守る者もあり、</p>
  </li>
  <li>
    <p>敵にむかって戦いをいどむ者もある。</p>
  </li>
</ol>
<ol>
  <li>
    <p>それとおなじように、主キリストの騎士のなかにも、宮廷すなわち<a href="#note-empyree" title="最高天" id="empyree"><ruby>最高天<rp>(</rp><rt>エンビュラエウム</rt><rp>)</rp></ruby></a>に住まい、つねに主のお相手をつとめ、主をたたえ慰める歌をうたい、</p>
    <p class="conversation">「聖なるかな、聖なるかな、聖なるかな、万軍の主なる天主」</p>
    <p class="conversation">「賛美と栄光と知恵が、世々かぎりなくわれらの神とともにありますように」（『黙示』７・７～１２）ととなえる者もあれば、</p>
    <p>町や国や村や城を守る者もある。</p>
  </li>
  <li>
    <p>これは、われわれを守るために遣わされた天使たちで、童貞や貞潔な人たちや夫婦、さらに修道士たちの家を見張る。
    『イザヤ書』に、</p>
    <p class="conversation">「イェルサレムよ、わたしは、あなたの城壁のうえに見張り人をおいた」（６２・６）とあるのが、それである。</p>
  </li>
  <li>
    <p>さらにまた、神の敵である悪魔と戦う者もある。『黙示録』に、</p>
    <p class="conversation">「天では大きな戦いが起こった。
    <mark>ミカエル</mark>とその配下の天使たちが竜と戦ったのである」とあるのが、</p>
    <p>それである（この天は戦う教会をさすという解釈がある）。</p>
  </li>
</ol>

<h4 id="anges-consolateurs" class="red">天使たちが悲しめる人間たちをなぐさめてくれるからである</h4>

<p>われわれが天使たちを尊崇しなくてはならない七番目の、そして最後の理由は、彼らが悲しめる人間たちをなぐさめてくれるからである。
『ザカリア書』には、</p>
<p class="conversation">「わたしにねんごろな慰めの言葉をかけてくれた天使」（１・１３）とあるし、</p>
<p>『トビト書』では、</p>
<p class="conversation">天使が「元気を出しなさい」（５・１０）と言ってトビアスをなぐさめる。</p>
<p>天使たちは、三通りのしかたで慰めをあたえる。</p>
<ol>
  <li>
    <p>まず、力と強さをあたえる。
    『ダニエル書』で、天使が地に伏していたダニエルにさわって、</p>
    <p class="conversation">「怖れるにはおよばない。
    安心しなさい。
    こころを強くし、勇気を出しなさい」（１０・１９）と言うのがそれである。</p>
  </li>
  <li>
    <p>つぎに、天使たちは、<ruby>焦<rp>(</rp><rt>あせ</rt><rp>)</rp></ruby>りやせっかちから守ってくれる。
    『詩篇』に、</p>
    <p class="conversation">「主は、あなたのために天使たちに命じて、あなたの歩むすべての道であなたを守らせる。
    天使たちは、その手であなたを支え、石に足を打ちつけることのないようにする」（９１・１１～１２）とある。</p>
  </li>
  <li>
    <p>さらに、天使たちは、悲しみをしずめ、やわらげてくれる。
    『ダニエル書』（３・１９以下）に、火のなかに投げこまれた三人の青年のところに主の御使いが舞いおりてきて、朝の風に吹かれたように炉のなかを涼しくしたという話が出てくるが、この物語は、そのことをあらわしているのである。</p>
  </li>
</ol>

