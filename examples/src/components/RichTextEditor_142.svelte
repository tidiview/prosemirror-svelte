<h3 id="victoire-michel" class="red">大天使ミカエルの勝利</h3>

<p>つぎに、この祝日は、聖<mark>ミカエル</mark>の勝利の日とも言われる。聖<mark><mark>ミカエル</mark></mark>をはじめ天使たちの勝利は、さまざまのものがある。</p>

<h4 id="victoire-sipontus" class="red">シポントゥムのキリスト教徒たちの勝利</h4>

<p>第一の勝利は、聖<mark>ミカエル</mark>が上述のシポントゥムの人びとにもたらした勝利である。
この勝利は、つぎのようなものであった。</p>
<p>聖<mark>ミカエル</mark>の洞穴の発見後しばらくたってから、当時まだ異教徒であったナポリの市民たちは、兵を送って、シポントゥムの町とそこから５０マイルほどはなれた<a href="#note-benevent" title="ベネヴェント" id="benevent">ベネヴェント</a>の町を攻撃した。</p>
<p>このとき、シボントゥムの司教は、三日間の休戦をもとめ、そのあいだ断食をして町の守護者である聖<mark>ミカエル</mark>にお助けを祈願するように、と町の人びとに助言した。</p>
<p>三日目の夜、聖<mark>ミカエル</mark>が司教にあらわれて、願いは聞きとどけられたと言い、勝利を約束し、翌朝<time datetime="10:00">１０時</time>に敵軍にむかって打ってでるようにと命じた。</p>
<p>さて、戦いがはじまると、ガルガーノ山がはげしく<ruby>鳴<rp>(</rp><rt>めい</rt><rp>)</rp>動<rp>(</rp><rt>どう</rt><rp>)</rp></ruby>し、おびただしい稲妻が降り、山頂のまわりに黒雲がたちこめた。</p>
<p>こうして、炎の矢とシポントゥム兵の剣のために６００名ものナポリ兵が死んだ。</p>
<p>生き残った兵士たちも、大天使<mark>ミカエル</mark>の力のほどを知り、キリスト教の信仰のまえに屈した。</p>

<h4 id="victoire-lucifer" class="red">ルキフェルに対する勝利</h4>

<p>聖<mark>ミカエル</mark>が得たニ番目の勝利は、彼が竜すなわち<a href="#note-lucifer" title="ルキフェル" id="lucifer">ルキフェル</a>とその一味を天国から突き落としたときの勝利である。</p>
<p>これについては、『ヨハネの黙示録』（１２・７）に書かれている言葉が引用される。</p>
<p class="conversation">「さて、天では大きな戦いが起こった。</p>
<p class="conversation"><mark>ミカエル</mark>とその配下の天使たちが竜と戦ったのである」つまり、ルキフェルが神と肩をならべようとしたので、天の軍勢の<ruby>旗<rp>(</rp><rt>き</rt><rp>)</rp>手<rp>(</rp><rt>しゅ</rt><rp>)</rp></ruby>である<mark>ミカエル</mark>があらわれて、ルキフェルとその一味を天から突き落とし、最後の審判の日まで暗い<ruby>中<rp>(</rp><rt>なか</rt><rp>)</rp>空<rp>(</rp><rt>そら</rt><rp>)</rp></ruby>に追放したのである。</p>
<p>彼らは、もはや天にも明るくて心地よい空中の上層の部分にも住むことを許されず、人間にひどい害をあたえかねない地上のわれわれのもとに住むこともできない。</p>
<p>彼らがいるところは、天と地のあいだの中空である。</p>
<p>頭上には彼らが追われた天の栄光があり、それを見ては後悔のほぞを嚙み、足下にはこの地上があり、彼らが追いだされたところへ人間たちがのぼっていくのを見てはげしい<ruby>羨<rp>(</rp><rt>せん</rt><rp>)</rp>望<rp>(</rp><rt>ぼう</rt><rp>)</rp></ruby>の念にさいなまれることになる。</p>
<p>もっとも、神のはからいによって、ときには地上に住み、われわれを試みることもある。</p>
<p>彼らを見たことのある信心ぶかい人たちによると、彼らは、まるで<ruby>蝿<rp>(</rp><rt>はえ</rt><rp>)</rp></ruby>のようにわたしたちの身のまわりを飛びまわっている。</p>
<p>というのは、彼らは、数がものすごく多く、<ruby>蝿<rp>(</rp><rt>はえ</rt><rp>)</rp></ruby>のように中空にむらがっているからだそうである。</p>
<p>これについて<a href="#note-haymon" title="ハイモ" id="haymon">ハイモ</a>は、</p>
<p class="conversation">「昔の哲学者たちも言い、現代の教師たちも信じているように、空中には悪魔や悪霊どもがうようよしている」と書いている。</p>
<p>彼らの数は、このょうにおびただしく多いが、それでも、<a href="#note-origene" title="オリゲネス" id="origene">オリゲネス</a>の見解によると、われわれが彼らのひとりやっつけるたびに、その軍勢は、すこしずつ減っていくという。</p>
<p>彼らのひとりがある信心ぶかい人間にやりこめられると、その人を2度とおなじ罪で誘惑することは許されないからである。</p>

<h4 id="victoire-anges" class="red">毎日天使たちがかちとる勝利</h4>

<p>三つ目の勝利は、天使たちがいまなお毎日われわれのために戦い、われわれを悪魔どもの誘惑から解放してくれるときにかちとる勝利である。</p>
<p>天使たちがわれわれを悪魔どもの誘惑から解放してくれるのには、三通りのやりかたがある。</p>
  <ul>
    <li>
      <p>第一のやりかたは、悪魔の力をいましめるのである。</p>
      <p>これについては、『黙示録』（１０・２～３）に、悪魔をしばりあげて底なしの淵に投げこんだ天使のことが記されている。</p>
      <p>『トビト書』（８・３）にも、上部エジプトの荒野で天使にしばりあげられた悪魔のことが出てくる。</p>
      <p>ところで、悪魔をしばるとは、悪魔の力を奪いとることにほかならない。</p>
    </li>
    <li>
      <p>天使がわれわれを誘惑から解きはなつ第二のやりかたは、われわれの欲望をしずめるのである。</p>
      <p>これについては、『創世記』（３２・２５）に、天使が<a href="#note-jacob" title="ヤコブ" id="jacob">ヤコブ</a>の<ruby>股<rp>(</rp><rt>また</rt><rp>)</rp></ruby>のつがいにふれると、たちまちそこが干からびたという話が記されている。</p>
    </li>
    <li>
      <p>もうひとつのやりかたは、われわれの意識に主のご受難の記憶をしっかりおぼえこませるのである。</p>
      <p>『黙示録』でひとりの天使が「われわれが神のしもべたちの<ruby>額<rp>(</rp><rt>ひたい</rt><rp>)</rp></ruby>に刻印を押してしまうまでは、大地も海も木もそこなってはならない」（６・３）とさけぶのが、それである。</p>
      <p>また、『エゼキエル書』に「イェルサレムの町のなかでおこなわれているすべての憎むべきことを嘆き悲しむ人びとの額に<a href="#note-tau" title="しるし" id="tau">しるし</a>をつけよ」（９・４）とあるのも、それである。</p>
      <p>というのは、このしるしとは、十字架の形をした文字のことだからである。</p>
      <p>そして、このしるしをつけられた人びとは、天使の<ruby>打<rp>(</rp><rt>ちょう</rt><rp>)</rp>擲<rp>(</rp><rt>ちゃく</rt><rp>)</rp></ruby>を怖れることはない。</p>
      <p>そのあとに、</p>
      <p class="conversation">「しかし、身にしるしのある者は殺してはならない」（９・６）と言われているのは、そのためである。</p>
    </li>
  </ul>

<h4 id="victoire-antechrist" class="red">反キリストにかちとる勝利</h4>

<p>四つ目の勝利は、大天使<mark>ミカエル</mark>が反キリストを滅ぼすときにかちとる勝利である。</p>
<p>『ダニエル書』（１２・１）にあるように、そのとき大いなる<ruby>君<rp>(</rp><rt>きみ</rt><rp>)</rp></ruby><mark>ミカエル</mark>は、反キリストから守り救ってくれる者として選ばれた人びとのまえに力づよく立つであろう。</p>
<p>すると、反キリストは、死んだふりをする。『黙示録』の「このけものの<ruby>頭<rp>(</rp><rt>あたま</rt><rp>)</rp></ruby>のひとつが傷を受けて、死んだように見えた」（１３・３）という言葉について、<ruby>注解書<rp>(</rp><rt>グロッサ</rt><rp>)</rp></ruby>はそう述べている。
そして、<time datetime="P3D">三日のあいだ</time>姿を隠していて三日目にあらわれて、〈われ復活せり〉と豪語するであろう。
すると、彼の手下の悪魔どもが、魔術を使って彼をはこびあげ、彼は、中空にのぼっていく。人びとはみな、これを見ておどろき、彼を拝む。</p>
<p>最後に彼は、オリヴ山に登るであろう。
そして、『テサロニケ人への第ニの手紙』の「主イエスは、この者を殺すであろう」（２・８）について<ruby>注解書<rp>(</rp><rt>グロッサ</rt><rp>)</rp></ruby>が述べているように、自分の幕屋の露台の、主とむかいあった場所に立つ。
そのとき<mark>ミカエル</mark>があらわれて、彼を殺すであろう。</p>
<p>グレゴリウスによると、『黙示録』の</p>
<p class="conversation">「さて、天では大きな戦いが起こった。</p>
<p class="conversation"><mark>ミカエル</mark>とその天使たちが竜と戦いを<ruby>挑<rp>(</rp><rt>いと</rt><rp>)</rp></ruby>んだのである。</p>
<p class="conversation">竜と使いたちも応戦したが、勝てなかった」</p>
<p>というくだりは、以上のような戦いとその勝利を言ったものと理解しなくてはならない。</p>
<p>つまり、この言葉は、聖<mark>ミカエル</mark>の3つの戦いをさしていると理解されるのである。</p>
<ol>
  <li>まず第一に、ルキフェルを天から突き落としたときの戦いである。</li>
  <li>つぎに、われわれを誘惑しようとする悪魔たちとの戦い。</li>
  <li>さらに、<mark>ミカエル</mark>は、いま述べたように、世の終わりのときに反キリストと戦うのである。</li>
</ol>

<h3 id="dedicace" class="red">聖別の祝日</h3>

<p>第三に、この日が聖<mark>ミカエル</mark>聖別の祝日ともよばれるのは、この日に聖<mark>ミカエル</mark>があのガルガーノ山上の洞穴はすでに聖別されて、自分に献じられていると告げたからである。</p>
<p>というのは、シポントゥムの町の人びとは、敵軍に大勝して町に帰ってからも、あの山上の洞穴に入るべきか、それともこの場所を聖別すべきかで迷っていたからである。</p>
<p>町の司教は、<a href="#note-pelagius" title="教皇ペラギウス" id="pelagius">教皇ペラギウス</a>の意見をもとめた。教皇は、</p>
<p class="conversation">「人間があの教会を聖別するのなら、勝利を得た日にするのが一番よいのだが、聖<mark>ミカエル</mark>にもお考えがあるかもしれないから、<ruby>思召<rp>(</rp><rt>おぼしめ</rt><rp>)</rp></ruby>しをお教えくださるようにお願いしなくてはならない」と答えた。</p>
<p>そこで、司教と町の人びとは、<time datetime="P3D">三日間</time>断食をして祈った。
すると、<time datetime="09-29">９月２９日</time>に聖<mark>ミカエル</mark>が司教にあらわれて、こう告げた。</p>
<p class="conversation">「わたしが建てた教会をあなたがたが聖別することはありません。</p>
<p class="conversation">というのは、建立者であるわたしが自分で聖別したからです」</p>
<p>そう言ってから、あす町の人びとといっしょに山に登り、洞穴に入って、わたしに祈りをささげ、わたしを町の守護者としなさい、と命じた。</p>
<p>さらに、聖別のしるしがほしいと言うのであれば、東のほうからわき道を洞穴まで登ってくるのです、そうすれば、ひとりの人間の足跡が大理石のうえに刻まれているのが見つかるでしょう、と告げた。</p>
<p>翌日、司教と町の人びとが山に登り、洞穴に足をふみ入れてみると、そこに三つの祭壇をもった大きな地下聖堂が見っかった。
三つの祭壇のうち、ふたつは南むき、ひとつは東むきであった。</p>
<p>この東むきの祭壇は、とくに荘厳で、赤い前帳がかけてあった。</p>
<p>人びとは、ここでおごそかにミサをおこない、ひとりずつ<a href="#note-communion" title="聖体を拝領した" id="communion">聖体を拝領した</a>。
<p>聖体の秘跡の制定である。
ミサがすむと、大よろこびしながら町に帰った。
司教は、洞穴の聖堂でいつも聖務をおこなう司祭と聖職者を定めた。</p>
<p>この洞穴には、たいへん甘味な清水が湧きでている。聖体を拝領してからこの清水を飲むと、いろんな病気が治る。</p>
<p>これら一部始終のことを聞いた教皇は、以後この日を聖<mark>ミカエル</mark>とすべての天使たちをたたえる祝日とするょうに命じた。</p>


