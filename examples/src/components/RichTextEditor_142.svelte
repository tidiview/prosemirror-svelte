<h2 id="chap139" class="red"><cite><ruby>黄金伝説<rp>(</rp><rt>レゲンダ・アウレア</rt><rp>)</rp></ruby></cite>の１３９章の文書：大天使聖ミカエル</h2>

<h3 id="name" class="red">聖ミカ工ルの名</h3>

<p><mark>ミカ工ル</mark>とは、〈だれが神に比べられようか〉という意味である。<a href="#note-gregorius" title="聖グレゴリウス" id="gregorius">聖グレゴリウス</a>は、こう書いている。「大きな奇跡が起こるときには、いつも<mark>ミカエル</mark>が派遣される。その<ruby>業<rp>(</rp><rt>わざ</rt><rp>)</rp></ruby>とその名前から、このような偉大な奇跡は神以外のだれもなしえないということが明らかになるためである」多くの偉大な奇跡が聖<mark>ミカエル</mark>のはたらきだとされるのは、このためである。</p>
<p>というのは、<a href="#note-daniel" title="ダニエル書" id="daniel">ダニエル</a>が書いているように、<mark>ミカエル</mark>は、「憎むべき<a href="#note-antechrist" title="破壊者" id="antechrist">破壊者</a>」があらわれるときに立ちあがり、選ばれた民を守ってくれるからである。</p>
<p>たとえば、彼は、竜とその手下どと戦い、彼らを天国から突き落とし、大勝利をおさめたし<a href="https://francois-vidit.com/docs/ja/mont-saint-michel/arch-michel/apocalypse#bataille" title="『黙示』１２の７～９">（『黙示』１２章・７節～９節）</a>、悪魔が<a href="#note-moise" title="モーセの遺体" id="moise">モーセの遺体</a>を<ruby>祀<rp>(</rp><rt>まつ</rt><rp>)</rp></ruby>って、ユダヤ人たちにこれを神として拝ませようとしたときも、遺体をめぐって悪魔と争った。聖人たちのたましいを受けとり、喜びの<ruby>楽園<rp>(</rp><rt>ぱらいそ</rt><rp>)</rp></ruby>にみちびいていくのも、<mark>ミカエル</mark>である。</p>
<p>彼は、もとユダヤ教会堂の守護者であったが、主は、キリスト教会の守護者とされた。</p>
<p>エジプト人に天罰をくだし、紅海をふたつに分け、イスラエルの民をみちびいて荒野をわたり、<a href="#note-canaan" title="カナアン" id="canaan"><ruby>約束の地<rp>(</rp><rt>カナアン</rt><rp>)</rp></ruby></a>につれていったのも、<mark>ミカエル</mark>であったと言われる（『出エジプト記』）。彼はまた、聖なる天の軍勢のなかではキリストの旗手をつとめる。</p>
<p>彼は、主の命令によって<a href="#note-oliviers" title="オリヴ山" id="oliviers">オリヴ山</a>上で<a href="#note-antechrist" title="破壊者" id="antechrist2">反キリスト</a>にを力をもって倒すであろう。死者たちは、彼の声によって復活すると言われている。
<a href="#note-jugement" title="最後の審判の日" id="jugement">最後の審判の日</a>に聖十字架と<ruby>聖<rp>(</rp><rt>せい</rt><rp>)</rp>釘<rp>(</rp><rt>てい</rt><rp>)</rp></ruby>と<ruby>聖<rp>(</rp><rt>せい</rt><rp>)</rp>槍<rp>(</rp><rt>そう</rt><rp>)</rp></ruby>といばらの冠をもってくるのも、彼であるとされている。</p>
<p>大天使聖<mark>ミカエル</mark>の祝日は、彼の出現と勝利と聖別と記憶の日とよばれている。</p>

<h3 id="apparitions" class="red">出現</h3>

<h4 id="garganus" class="red">ガルガヌスと雄牛の出現</h4>

<p>聖ミカルの出現は、いろいろある。最初の出現は、<a href="#note-gargano" title="ガルガーノ山" id="gargano">ガルガーノ山</a>上であった。</p>
<p>ガルガーノはアプリア地方の山で、シポントウムという町の近くにある。主の<time datetime="390">紀元３９０年ごろ</time>、この町に<ruby>ガルガヌス<rp>(</rp><rt>Garganus</rt><rp>)</rp></ruby>という名の男が住んでいた。
山の名は、この男の名前にちなんでつけられた。
しかし、男の名前がこの山の名からとられたとしている本もある。</p>
<p>ガルガヌスは、数えきれないほど多くの牛や羊を飼っていた。</p>
<p>あるとき、彼の牛や羊が山腹で草を<ruby>食<rp>(</rp><rt>は</rt><rp>)</rp></ruby>んでいると、一頭の雄牛が群れからはなれて、山頂に登っていった。ほかの牛や羊が山から下ってきたときも、この雄牛だけもどっていないことがわかった。</p>
<p>そこで男は、大勢の牧童といっしょに雄牛をもとめて道なき道を登っていき、ついに山頂の、とある洞穴の入口でこの牛を見つけた。</p>
<p>男は、群れからはなれて勝手な行動をしたことに腹をたてて、雄牛めがけて毒矢を放った。</p>
<p>ところが、矢は、風になぶられかのように向きを変えて、<ruby>射<rp>(</rp><rt>い</rt><rp>)</rp>手<rp>(</rp><rt>て</rt><rp>)</rp></ruby>のほうに舞いもどってきた。</p>
<p>町の人びとは、これを知ってたいへんおどろき、この大きな奇跡はなにを意味しているのでしょうか、と司教にたずねた。</p>
<p>司教は、<time datetime="P3D">紀三日間</time>の<ruby>斎<rp>(</rp><rt>さい</rt><rp>)</rp>食<rp>(</rp><rt>じき</rt><rp>)</rp></ruby>（断食）を指示し、これにはどういう意味があるのか主にお伺いすることにしましょう、と答えた。</p>
<p>そのとおりにしていると、聖<mark>ミカエル</mark>が司教にあらわれ、こう告げた。</p>
<p class="conversation">「あの場が自分の矢に射られたのは、わたしの意志によることです。
かく言うわたしは、大天使<mark>ミカエル</mark>であって、地上のこの場所が気に入って、ここに住み、この地を守ることにしました。
よって、あの奇跡でもって、自分がこの土地の守護者であり番人であることを知らせようとしたのです」</p>
<p>そこで、司教と町の住民たちは、ただちに行列をくんで、奇跡の起こった場所に行き、洞穴のまえでうやうやしくお祈りをささげた。
しかし、洞穴のなかに入ることははばかった。</p>

<h4 id="mont-tombe" class="red">トウンバ岩山での出現</h4>

<p>二度目の出現は、主の<time datetime="710">紀元７１０年ごろ</time>のことであったと言われている。</p>
<p>アプリカの町から六マイルほどはなれた海辺の<a href="#note-tumba" title="トウンバ" id="tumba">トウンバ</a>というところで、聖<mark>ミカエル</mark>がこの町の司教にあらわれ、この地に教会を建て、ガルガーノの山でおこなわれているように、わたしを記念する場所としなさい、と命じた。</p>
<p>司教がどこに教会を建てたものかと思案していると、泥棒どもが隠した雄牛の見つかる場所に建てなさいというお告げがあった。</p>
<p>また、司教がどれほどの広さの教会にしたものかと迷っていると、雄牛の足跡が地面に残っているだけの広さにしなさいというお告げがあった。</p>
<p>ところが、この場所には、人力では動かせないほどの大きな岩がふたっあった。</p>
<p>すると、聖<mark>ミカエル</mark>がある男にあらわれ、くだんの場所に行って岩を動かしなさいと命じた。
そこで、男は、出かけていって、ふたつの岩をかるがると持ちあげた。</p>
<p>こうして教会ができあがると、聖<mark>ミカエル</mark>がガルガーノの教会の祭壇に掛けておいた前帳の一部と、聖<mark>ミカエル</mark>がそのうえに立っていた大理石の一部がはこばれてきた。</p>
<p>ところで、この土地には水がなかった。
そこで、人びとは、これまた聖<mark>ミカエル</mark>の命令で固い岩に穴を<ruby>穿<rp>(</rp><rt>うが</rt><rp>)</rp></ruby>った。
すると、水が<ruby>滾<rp>(</rp><rt>こん</rt><rp>)</rp>々<rp>(</rp><rt>こん</rt><rp>)</rp></ruby>と湧きだしてきた。
今日、ここからゆたかな水が流れでている。</p>
<p>聖<mark>ミカエル</mark>の慈悲のあらわれである。
この出現、<time datetime="10-16">１０月１６日</time>に祝われる。</p>
<p>おなじ町で、もうひとつ記憶にあたいするような奇跡が起こったと言われる。</p>
<p>この山は、周囲を海にかこまれているが、聖<mark>ミカエル</mark>の日には、二度海の潮が引いて、自由に渡れるようになる。</p>
<p>昔のことだから、実際そういうことがあったのであろう。</p>
<p>あるとき、大勢の人びとがこの教会に<ruby>詣<rp>(</rp><rt>もう</rt><rp>)</rp></ruby>でた。なかにひとり出産まちかの婦人がまじっていた。</p>
<p>ところが、いったん引いたかに見えた<ruby>波<rp>(</rp><rt>は</rt><rp>)</rp>浪<rp>(</rp><rt>ろう</rt><rp>)</rp></ruby>が、突然またものすごい勢いでもどってきた。人びとはみな、怖れおののいて岸のほうに逃げ帰った。</p>
<p><ruby>身<rp>(</rp><rt>み</rt><rp>)</rp>重<rp>(</rp><rt>おも</rt><rp>)</rp></ruby>の婦人だけは、逃げおくれて、波にのまれてしまった。</p>
<p>しかし、大天使聖<mark>ミカエル</mark>が彼女をしっかりつかまえていてくれたので、彼女は、海のなかで無事子供を生みおとし、腕に抱いて乳を飲ませた。</p>
<p>そして、潮がまた引いて通り道ができると、うれしそうに子供といっしょに水のなかからあがってきた。</p>

<h4 id="chateau-saint-ange" class="red">聖天使城での出現</h4>

<p>三度目の出現は、ある本によると、教皇グレゴリウスの時代にローマで起こった。</p>
<p>というのは、教皇が俗に〈よこね〉とよばれているペストの流行を終わらせるために大がかりな祈願行列をくりだし、人びとの救霊のために祈っていたとき、ハドリアヌス霊廟とよばれていた城のうえにひとりの天使が立っているのが見えた。</p>
<p>天使は、血にぬれた剣をぬぐい、<ruby>鞘<rp>(</rp><rt>さや</rt><rp>)</rp></ruby>におさめた。</p>
<p>教皇は、これを見て、神が自分の祈りを聞きとどけてくださったのだと理解し、その場所に天使をたたえて教会を建てた。それにちなんで、この城も、今日にいたるまで 〈<a href="#note-saintange" title="聖天使城" id="saintange2"><ruby lang="ja">聖天使城<rp>(</rp><rt lang="it">Castel&#160;Sant'Angelo</rt><rp>)</rp></ruby></a>〉 とよばれている。</p>
<p>この出現は、聖<mark>ミカエル</mark>がガルガーノの山にあらわれて、シポントゥムの町の人たちに勝利をあたえた出現とおなじく、５月８日に祝われる。</p>

